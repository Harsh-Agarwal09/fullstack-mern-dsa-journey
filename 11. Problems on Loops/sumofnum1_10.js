// Write a program to calculate sum of all the no. from 1 - 10.

let sum = 0;
for (let i = 1; i <= 10; i++) {
    sum += i;
}
console.log('Sum of all no. from 1 - 10 is: ' + sum);

// Using while loop
let sum1 = 0;
let j = 1;
while (j <= 10) {
    sum1 += j;
    j++;
}
console.log('Sum of all no. from 1 - 10 is: ' + sum1);